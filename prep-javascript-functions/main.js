// addTwoNumbers
function addTwoNumbers(num1, num2) {
  return num1 + num2;
}

console.log(addTwoNumbers(2, 2));

// covertHoursToMinutes

function convertHoursToMinutes(num) {
  return num * 60;
}

console.log(convertHoursToMinutes(2));

